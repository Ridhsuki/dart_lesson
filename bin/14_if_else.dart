// * Control Flow: if, else if, else
// Used to make decisions in your program

void main() {
  int score = 75; // change this value to test different conditions

  if (score >= 90) {
    print('Grade: A');
  } else if (score >= 80) {
    print('Grade: B');
  } else if (score >= 70) {
    print('Grade: C');
  } else {
    print('Grade: D or below');
  }
}
