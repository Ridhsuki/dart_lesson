//! Type Data in Dart

//? #1 String

void main() {
  //Normal String
  String name = 'Basuki Ridho';
  print(name); // output = Basuki Ridho

  // String Interpolation => use ${variable name}
  int age = 18;
  print('My name is $name and I am ${age + 1} years old in 2025');  // output = My name is Basuki Ridho and I am 19 years old in 2025
}
