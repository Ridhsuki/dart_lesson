// * Comparison Operators in Dart
// These operators are used to compare values.
// They return true or false.

void main() {
  int a = 10;
  int b = 5;

  print('a = $a, b = $b');

  // Equal to
  print('a == b: ${a == b}'); // false

  // Not equal to
  print('a != b: ${a != b}'); // true

  // Greater than
  print('a > b: ${a > b}'); // true

  // Less than
  print('a < b: ${a < b}'); // false

  // Greater than or equal to
  print('a >= b: ${a >= b}'); // true

  // Less than or equal to
  print('a <= b: ${a <= b}'); // false
}

// - These operators return true or false.
// - You can use them in if/else statements to control your program.
// - They help compare numbers, strings, and more.
